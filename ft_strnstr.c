#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	char	*substr;
	char	*ret;

	substr = ft_strsub(needle, 0, len);
	ret = ft_strstr(haystack, substr);
	ft_strdel(&substr);
	return (ret);
}

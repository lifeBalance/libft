#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const char	*str1;
	const char	*str2;

	str1 = (const char *)s1;
	str2 = (const char *)s2;
	if (ft_strequ(str1, str2))
		return (0);
	else
		return (ft_strncmp(str1, str2, n));
}

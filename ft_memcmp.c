#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	if (ft_strequ((char *)s1, (char *)s2))
		return (0);
	else
		return (ft_strncmp((char *)s1, (char *)s2, n));
}

#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*substr;

	substr = ft_strnew(len);
	if (!substr)
		return (NULL);
	ft_strncpy(substr, s + start, len);
	return (substr);
}

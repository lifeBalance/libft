#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;

	dup = (char *)malloc(ft_strlen(s1) + 1);
	if (!dup)
		exit (EXIT_FAILURE);
	return (ft_strcpy(dup, s1));
}
